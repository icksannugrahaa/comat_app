class Event {
  final String title;
  final String description;
  final String place;
  final String date;
  final String timeStart;
  final String timeEnd;
  final String image;
  final int limit;
  final int remains;
  final bool status;

  Event({this.title, this.description, this.place, this.date, this.timeStart, this.timeEnd, this.image, this.limit, this.remains, this.status});
}